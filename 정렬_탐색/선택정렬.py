## 리스트의 가장 작은 숫자를 선택해서 앞쪽으로 옮기는 방법
## 시간복잡도: O(n2)
## 구성과 상관없이 무조건 중첩for문


arr = [9,5,2,7,4,1,3,6,8]  # 정렬할 리스트 선언

n = len(arr)  # 리스트의 길이(원소 개수)를 n에 저장

# 0번째부터 n-2번째 인덱스까지 반복 (마지막 원소는 자동으로 정렬됨)
for i in range(n-1):
    min_idx = i  # 현재 위치를 최소값의 인덱스로 가정

    # i+1번째부터 마지막 원소까지 반복하며 최소값 찾기
    for j in range(i+1, n):
        if arr[j] < arr[min_idx]:  # 더 작은 값이 있으면
            min_idx = j           # 그 인덱스를 최소값 인덱스로 저장

    # 찾은 최소값을 현재 위치(i번째)와 교환
    arr[i], arr[min_idx] = arr[min_idx], arr[i]

# 정렬이 끝난 후 arr는 오름차순으로 정렬됨